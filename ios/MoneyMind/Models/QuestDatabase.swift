enum QuestDatabase {

    static var totalQuests: Int { allQuests.count }

    static func quest(byID id: String) -> QuestDefinition? {
        allQuests.first { $0.id == id }
    }

    static func quests(forCategory category: QuestCategory) -> [QuestDefinition] {
        allQuests.filter { $0.category == category }
    }

    static func quests(forZone zone: QuestZone) -> [QuestDefinition] {
        allQuests.filter { $0.zone == zone }
    }

    static func quests(forChain chainID: String) -> [QuestDefinition] {
        allQuests.filter { $0.chainID == chainID }.sorted { ($0.chainIndex ?? 0) < ($1.chainIndex ?? 0) }
    }

    static func quests(forCadence cadence: QuestCadence) -> [QuestDefinition] {
        allQuests.filter { $0.cadence == cadence }
